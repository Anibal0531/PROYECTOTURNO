

public class NodoA {
	public Asesor asesor;
	public NodoA next;
	public Asesor tamaño;
	public NodoA head;
	
	public Asesor getAsesor() {
		return asesor;
	}
	public void setAsesor(Asesor asesor) {
		this.asesor = asesor;
	}
	public NodoA getNext() {
		return next;
	}
	public void setNext(NodoA next) {
		this.next = next;
	}
	public Asesor getTamaño() {
		return tamaño;
	}
	public void setTamaño(Asesor tamaño) {
		this.tamaño = tamaño;
	}
	public NodoA getHead() {
		return head;
	}
	public void setHead(NodoA head) {
		this.head = head;
	}
	

}
